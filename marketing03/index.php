	{
		alert("업체명을 입력해주세요.");
		form.qa_subject.focus();
		return;
	}
	if(form.qa_4.value=="")
	{
		alert("예산을 입력해주세요.");
		form.qa_4.focus();
		return;
	}

	if(form.qa_hp.value=="")
	{
		alert("연락처를 입력해주세요.");
		form.qa_hp.focus();
		return;
	}

	if(form.qa_content.value=="")
	{
		alert("문의내용을 입력해주세요.");
		form.qa_content.focus();
		return;
	}

